class Ex03{
	public static void main(String[] agrs){
	// 산술 연산자 : 사칙연산 + mod
		int su1 = 9;
		int su2 = 4;
		int result = 0;
		result = su1 + su2;
		System.out.println(su1 + " + " + su2 + " = "+ result);
		result = su1 - su2;
		System.out.println(su1 + " - " + su2 + " = "+ result);
		result = su1 * su2;
		System.out.println(su1 + " * " + su2 + " = "+ result);
		result = su1 / su2;
		System.out.println(su1 + " / " + su2 + " = "+ result);
		result = su1 % su2;
		System.out.println(su1 + " % " + su2 + " = "+ result);
	}
}